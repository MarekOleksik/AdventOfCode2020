package day7.part1;

public class ChildrenBags {
    int quantityOfBags;
    String nameOfBags;

    public ChildrenBags(int quantityOfBags, String nameOfBags) {
        this.quantityOfBags = quantityOfBags;
        this.nameOfBags = nameOfBags;
    }

    public int getQuantityOfBags() {
        return quantityOfBags;
    }

    public void setQuantityOfBags(int quantityOfBags) {
        this.quantityOfBags = quantityOfBags;
    }

    public String getNameOfBags() {
        return nameOfBags;
    }

    public void setNameOfBags(String nameOfBags) {
        this.nameOfBags = nameOfBags;
    }
}
